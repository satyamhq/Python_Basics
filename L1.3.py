# Printing text (string values)
print("Satyam Kumar")
print("I am Learning Python.")

# Printing multiple messages in one print statement
print("I am Satyam Kumar.", "I am Learning Python")

# Printing a number (integer value)
print(31)

# Printing the result of an arithmetic expression
print(31 + 9)   # Output: 40

# Printing text that looks like an expression (no calculation)
print("31+9")   # Output: 31+9
