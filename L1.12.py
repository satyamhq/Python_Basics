# Input in Python
# ----------------------------------------
# input() is used to accept values from the user (keyboard).
# Note: input() always returns a STRING.
#       Use int() or float() for numeric conversions.

# String input
name = input("Enter your name: ")
print("Name:", name)

# Integer input
age = int(input("Enter your age: "))
print("Age:", age)

# Float input
price = float(input("Enter your net worth: "))
print("Net Worth:", price)

# Displaying all inputs together
print("My name is", name, ", I am", age, "years old and my net worth is", price)
