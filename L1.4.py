# Variables in Python
# ------------------------------------------------------
# A variable is a name given to a memory location in a program.
# It is used to store data values (like text, numbers, etc.).

# Creating variables
name = "Satyam"       # str → text
age = 18              # int → whole number
price = 31.11         # float → decimal number

# Printing variable values
print(name)           # Output: Satyam
print(age)            # Output: 18
print(price)          # Output: 31.11

# Displaying with context
print("My name is:", name + ".")                  # visit L1.5.py
print("I am", age, "years old.")
print("Satyam Kumar's networth is:", price, "Billion.")

# Copying value of one variable into another
age1 = age
print(age1)           # Output: 18

print(age+1)          # Output: 19
