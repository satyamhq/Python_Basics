name = "Satyam Kumar"   # str → text
age = 18                # int → whole number
price = 31              # int → whole number
networth = 31.11        # float → decimal number
is_student = True       # bool → True/False
unknown = None          # NoneType → represents no value

# Check data types
print(type(name))       # <class 'str'>
print(type(age))        # <class 'int'>
print(type(price))      # <class 'int'>
print(type(networth))   # <class 'float'>
print(type(is_student)) # <class 'bool'>
print(type(unknown))    # <class 'NoneType'>


