# Python Tokens
# ----------------------------------------------------
# 1. Keywords   → Reserved words (cannot be used as names)
# 2. Identifiers → Names given to variables, functions, classes, etc.
# 3. Literals    → Constant values (numbers, strings, booleans, etc.)
# 4. Operators   → Symbols that perform operations (+, -, *, /, etc.)
# 5. Punctuators/Separators → Special symbols ((), {}, [], :, ,, .)
